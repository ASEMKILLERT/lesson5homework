package com.company;

public class Boss {
    private int bHealth;
    private int bDamage;
    private String bDef;

    public int getbHealth() {
        return bHealth;
    }
    public void setbHealth(int bHealth) {
        this.bHealth = bHealth;
    }
    public int getbDamage() {
        return bDamage;
    }

    public void setbDamage(int bDamage) {
        this.bDamage = bDamage;
    }

    public String getbDef() {
        return bDef;
    }
    public void setbDef(String bDef){
        this.bDef = bDef;
    }
}
